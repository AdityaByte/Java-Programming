package com.java.basics.operators;

// Assignment operator (=) is used for assigning values
public class AssignmentOperator {
    public static void main(String[] args) {
        int a = 10;
        a = 12;
        // Firstly we assign the value of a to 10 then we reassign it to 12 using assignment operator
        System.out.println(a); // It will print out 12
    }
}
