package com.java.basics.operators;

// Operand -  A value or expression that is used to perform an operation.
public class Operand {
    public static void main(String[] args) {
        int a = 10;
        int b = 12;
        System.out.println(a + b); // Here a and b are operands and + is an operator so a and b are the values whose value has to be changed using the operator or operation.
    }
}