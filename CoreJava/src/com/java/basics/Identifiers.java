package com.java.basics;

// Identifiers can be defined as the unique name that we have give to things for identifying them.
public class Identifiers {

    public static void main(String[] args){
        // Like we have created a class above so the jvm identify that and create a bytefile for that using its name.
        // Here we are creating a variable using an identifier.
        /* Rule that we should follow which naming an identifier -
         * 1. It's name should not start from a numeric digit or symbol
         * 2. It doesn't contain any white space.
         * 3. Pascel Convention for classes and camelCase convention for variables and methods.
         */

        int a = 10; // Here a is the identifier so we identify it as a is a variable for type integer.
    }

}
