package com.java.controlflow.sequential;

// Sequential Control Flow
// It is the default control flow in which the program runs line by line is called the sequential control flow.
// In other words we can say that - The default mode of execution, where the program runs line by line, from top to bottom.
public class Main {
    public static void main(String[] args) {
        // Here in this case the program runs the first line then the second line so it is sequential control flow.
        System.out.println("HEllo");
        System.out.println("Aditya");
    }
}
